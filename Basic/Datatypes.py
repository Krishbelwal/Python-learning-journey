# There are mainly 6 data types in python-
# Numeric Data Types - int ,float , complex , sequenced(string , tuple, list, ) ,set, mapping(dict) , bool, none

# 1️⃣ Numeric Data Types
# int (Integer) → जैसे 5, -10, 1000
# float (Decimal Number) → जैसे 3.14, -2.5, 0.0001
# complex (Complex Numbers) → जैसे 3 + 4j, -2 - 5j
# x = 10       # int
# y = 3.14     # float
# z = 2 + 3j   # complex

# 2️⃣ Sequence Data Types-
# str (String) → "Hello", 'Python' , '''name'''
# Ex-
# a = 'name'
# a = "name"
# a = '''name'''
# list → [1, 2, 3], ["apple", "banana"]

# tuple → (1, 2, 3), ("red", "blue")


# s = "Hello"      # String
# l = [1, 2, 3]    # List
# t = (4, 5, 6)    # Tuple


